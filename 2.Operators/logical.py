'''
logical operators(To compare multiple conditions)
AND: All are True, 
OR: One is True,
NOT: True to false and false to true
, NOT'''

#And 
physics=100
chemistry= 20

print(physics> 33 and chemistry > 33) # Output False
print(physics> 33 or chemistry > 33) # Output True

print(not(physics> 33) and chemistry > 33) # False
print(not(physics> 33 and chemistry > 33)) # True





